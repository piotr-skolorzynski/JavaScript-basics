// logical operators

let myAge = 25;

// && means and

if (myAge >= 18 && myAge <= 30) {
    document.write('You can come, you cool dude');
} else {
    document.write('you aint coming!');
}

// || means or

if (myAge < 18 || myAge > 30 || myAge === 25) {
    document.write('You can come, you cool dude');
} else {
    document.write('you aint coming!');
}

